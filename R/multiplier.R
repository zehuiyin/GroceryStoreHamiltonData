#' Multiplying two numerical values or vectors
#'
#' @param x a numerical value or vector
#' @param y a numerical value or vector
#'
#' @return a numerical value or vector
#' @export
#'
#' @examples
#' multiplier(2, 5)
#' multiplier(c(2, 5), c(3, 8))
multiplier <- function(x, y){
  x * y
}
